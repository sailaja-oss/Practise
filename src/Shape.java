public class Shape {

        private double height;
        private double width;

        // Set height and width
        public void setValues(double height, double width)
        {
            this.height = height;
            this.width = width;
        }


        public double getHeight()
        {
            return height;
        }

        //Get width
        public double getWidth()
        {
            return width;
        }
    }


